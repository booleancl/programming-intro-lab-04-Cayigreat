# El for es una estructura de control que nos permite realizar la misma función, pero con elementos diferentes cada vez
# Se les llama también "Loops" (estructura de control)

veggies = ['Broccoli', 'Celery', 'Lettuce', 'Carrot']

for veggie in veggies:
    print('I need to eat more', veggie)

fruits = ['Apple','Strawberry','Banana', 'Orange']
for fruit in fruits:
    print('I love to eat', fruit)
     

