# Todos estos valores pertenecen a algún tipo
# Para Python los tipos son las denominadas clases
# Estos tres ejemplos son respectivamente

 # int: números enteros sin parte fraccionaria
print (type (2))
# float: para números con parte fraccionaria
print (type (7.0))
# str: para las cadenas de caracteres o simplemente texto (string)
print (type ("Hola mundo"))
